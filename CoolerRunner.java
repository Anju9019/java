public class CoolerRunner {

    public static void main(String[] Cooler) {
        System.out.println("Main is started");

        Cooler.onOrOff();           
        Cooler.increaseSpeed();     
        Cooler.increaseSpeed();     
        Cooler.decreaseSpeed();     
        Cooler.onOrOff();         
        Cooler.increaseSpeed();      

        System.out.println("Main is ended");
    }
}
